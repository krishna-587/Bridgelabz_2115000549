import java.util.*;
public class area {
    public static void main(String[] args) {
     Scanner sc = new Scanner(System.in);
     System.out.print("Enter the radius: ");
     double radius = sc.nextDouble();
     double area = (22.0 / 7) * radius * radius;
     System.out.println("Area of circle: " + area);
    }
        
}
